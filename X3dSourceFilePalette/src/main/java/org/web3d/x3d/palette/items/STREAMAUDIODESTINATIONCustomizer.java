  @Override
  public void unloadInput() throws IllegalArgumentException
  {
    unLoadDEFUSE();
    
    streamAudioDestination.setStreamIdentifier(streamIdentifierTF.getText().trim()); // TODO consider JComboBox is practice warrants that
    
    streamAudioDestination.setMediaDeviceID        (mediaDeviceIDTF.getText().trim());
    streamAudioDestination.setChannelCountMode     (channelCountModeComboBox.getSelectedItem().toString());
    streamAudioDestination.setChannelInterpretation(channelInterpretationComboBox.getSelectedItem().toString());
    streamAudioDestination.setDescription          (descriptionTF.getText().trim());
    streamAudioDestination.setEnabled              (enabledCB.isSelected());
    streamAudioDestination.setGain                 (gainTF.getText().trim());
  }
