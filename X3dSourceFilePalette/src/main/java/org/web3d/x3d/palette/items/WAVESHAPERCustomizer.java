    waveShaper.setDescription          (descriptionTF.getText().trim());
    waveShaper.setEnabled              (enabledCB.isSelected());
    waveShaper.setGain                 (gainTF.getText().trim());
    waveShaper.setStartTime            (startTimeTF.getText().trim());
