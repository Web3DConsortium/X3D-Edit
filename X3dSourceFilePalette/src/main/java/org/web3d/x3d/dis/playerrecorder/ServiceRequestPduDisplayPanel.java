  public void setData(ServiceRequestPdu pdu)
  {
    pduHeaderDisplayPanel1.setData(pdu);
    requestingEntityIDDisplayPanel.setData(pdu.getRequestingEntityID());
    servicingEntityIDDisplayPanel.setData(pdu.getServicingEntityID());
    serviceTypeRequestTF.setText(pdu.getServiceTypeRequested().getDescription());
    numberSupplyTypesTF.setText("" + pdu.getSupplies().size());
    paddingTF.setText("" + pdu.getPadding1());

    List<SupplyQuantity> lis = pdu.getSupplies();
    suppliesPanel.removeAll();
    for (SupplyQuantity sq : lis) {
      SupplyQuantityRecordDisplayPanel pan = new SupplyQuantityRecordDisplayPanel();
      pan.setData(sq);
      suppliesPanel.add(pan);
    }
  }

