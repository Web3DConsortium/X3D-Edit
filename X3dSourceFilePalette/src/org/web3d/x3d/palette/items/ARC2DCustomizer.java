    private void normalizeStartAngleButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_normalizeStartAngleButtonActionPerformed
    {//GEN-HEADEREND:event_normalizeStartAngleButtonActionPerformed
        checkAngles(true);
        double angle;

        angle = new SFDouble(startAngleTF.getText()).getValue();
        if (angle == -0.0)
        {
            angle = 0.0;
        }
        while (angle < -2.0 * Math.PI) // note valid range [-2pi .. 2pi] for this field
        {
            angle += 2.0 * Math.PI;
        }
        while (angle > 2.0 * Math.PI)
        {
            angle -= 2.0 * Math.PI;
        }
        startAngleTF.setText(radiansFormat.format(angle));
        startAngleTF.setToolTipText(radiansFormat.format(angle) + " radians = " + singleDigitFormat.format(angle * 180.0 / Math.PI) + " degrees");
}//GEN-LAST:event_normalizeStartAngleButtonActionPerformed

