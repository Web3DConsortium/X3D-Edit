private void normalizeRotationValuesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_normalizeRotationValuesButtonActionPerformed

    double angle; // n.b. following normalizations allow range -2pi..2pi radians for simpler authoring
    checkDiskAngle(true);
    checkMinAngle(true);
    checkMaxAngle(true);
    checkOffsetAngle(true);

    angle = Double.valueOf(diskAngleTF.getText());
    if (angle == -0.0) {
        angle = 0.0;
    }
    while (angle < -2.0 * Math.PI) {
        angle += 2.0 * Math.PI;
    }
    while (angle > 2.0 * Math.PI) {
        angle -= 2.0 * Math.PI;
    }
    diskAngleTF.setText(radiansFormat.format(angle));
    diskAngleTF.setToolTipText(radiansFormat.format(angle) + " radians = " + singleDigitFormat.format(angle * 180.0 / Math.PI) + " degrees");

    angle = Double.valueOf(minAngleTF.getText());
    if (angle == -0.0) {
        angle = 0.0;
    }
    while (angle < -2.0 * Math.PI) {
        angle += 2.0 * Math.PI;
    }
    while (angle > 2.0 * Math.PI) {
        angle -= 2.0 * Math.PI;
    }
    minAngleTF.setText(radiansFormat.format(angle));
    minAngleTF.setToolTipText(radiansFormat.format(angle) + " radians = " + singleDigitFormat.format(angle * 180.0 / Math.PI) + " degrees");

    angle = Double.valueOf(maxAngleTF.getText());
    if (angle == -0.0) {
        angle = 0.0;
    }
    while (angle < -2.0 * Math.PI) {
        angle += 2.0 * Math.PI;
    }
    while (angle > 2.0 * Math.PI) {
        angle -= 2.0 * Math.PI;
    }
    maxAngleTF.setText(radiansFormat.format(angle));
    maxAngleTF.setToolTipText(radiansFormat.format(angle) + " radians = " + singleDigitFormat.format(angle * 180.0 / Math.PI) + " degrees");

    angle = Double.valueOf(offsetAngleTF.getText());
    if (angle == -0.0) {
        angle = 0.0;
    }
    while (angle < -2.0 * Math.PI) {
        angle += 2.0 * Math.PI;
    }
    while (angle > 2.0 * Math.PI) {
        angle -= 2.0 * Math.PI;
    }
    offsetAngleTF.setText(radiansFormat.format(angle));
    offsetAngleTF.setToolTipText(radiansFormat.format(angle) + " radians = " + singleDigitFormat.format(angle * 180.0 / Math.PI) + " degrees");
    
        double normalizationFactor, x, y, z;

        x = new SFFloat(rotationXaxisTF.getText()).getValue();
        y = new SFFloat(rotationYaxisTF.getText()).getValue();
        z = new SFFloat(rotationZaxisTF.getText()).getValue();
        angle = new SFFloat(rotationAngleTF.getText()).getValue();
        normalizationFactor = Math.sqrt(x * x + y * y + z * z);
        if (normalizationFactor == 0.0)
        {
            NotifyDescriptor descriptor = new NotifyDescriptor.Message(
                    "Found zero-magnitude axis for rotation, reset to 0 1 0", NotifyDescriptor.WARNING_MESSAGE);
            DialogDisplayer.getDefault().notify(descriptor);
            rotationXaxisTF.setText("0");
            rotationYaxisTF.setText("1");
            rotationZaxisTF.setText("0");
        }
        else
        {
            rotationXaxisTF.setText(fiveDigitFormat.format(x / normalizationFactor));
            rotationYaxisTF.setText(fiveDigitFormat.format(y / normalizationFactor));
            rotationZaxisTF.setText(fiveDigitFormat.format(z / normalizationFactor));
        }
        if (angle == -0.0)
        {
            angle = 0.0;
        }
        while (angle < 0.0)
        {
            angle += 2.0 * Math.PI;
        }
        while (angle > 2.0 * Math.PI)
        {
            angle -= 2.0 * Math.PI;
        }
        rotationAngleTF.setText(radiansFormat.format(angle));
        rotationAngleTF.setToolTipText(radiansFormat.format(angle) + " radians = " + singleDigitFormat.format(angle * 180.0 / Math.PI) + " degrees");
}//GEN-LAST:event_normalizeRotationValuesButtonActionPerformed

