  @Override
  public void unloadInput() throws IllegalArgumentException
  {
    unLoadDEFUSE();
    
    lod.setBboxCenterX(bboxCenterXTF.getText().trim());
    lod.setBboxCenterY(bboxCenterYTF.getText().trim());
    lod.setBboxCenterY(bboxCenterZTF.getText().trim());
    lod.setBboxSizeX(bboxSizeXTF.getText().trim());
    lod.setBboxSizeY(bboxSizeYTF.getText().trim());
    lod.setBboxSizeZ(bboxSizeZTF.getText().trim());
    lod.setCenterX(centerXTF.getText().trim());
    lod.setCenterY(centerYTF.getText().trim());
    lod.setCenterZ(centerZTF.getText().trim());
    lod.setForceTransitions(forceTransitionsCB.isSelected());
    lod.setRange(rangeTF.getText().trim());    
  }
  

