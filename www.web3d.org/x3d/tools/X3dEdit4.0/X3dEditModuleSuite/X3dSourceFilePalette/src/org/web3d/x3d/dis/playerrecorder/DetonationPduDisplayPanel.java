  public void setData(DetonationPdu pdu)
  {
    this.pduHeaderDisplayPanel1.setData(pdu);
    this.firingEntityIDPanel.setData(pdu.getSourceEntityID());
    this.TargetEntityIDDisplayPanel.setData(pdu.getTargetEntityID());
    this.MunitionIDDisplayPanel.setData(pdu.getExplodingEntityID());

    this.eventIDDisplayPanel.setData(pdu.getEventID());
    this.velocityPanel.setData(pdu.getVelocity());
    this.locInWorldPanel.setData(pdu.getLocationInWorldCoordinates());
    this.burstDescriptorDisplayPanel1.setData(pdu.getDescriptor());
    this.locInEntityCoordPanel.setData(pdu.getLocationOfEntityCoordinates());
    this.detonationResultTF.setText(pdu.getDetonationResult().getDescription());
    this.paddingTF.setText("" + pdu.getPad());
    this.articulationParametersDisplayPanel1.setData(pdu.getVariableParameters().size(), pdu.getVariableParameters());
  }

