  public void setData(SignalPdu pdu)
  {
    pduHeaderDisplayPanel1.setData(pdu);
    
    EntityID eid = new EntityID();
    
    eid.setApplicationID(pdu.getHeader().getRadioReferenceID().getApplicationID());
    eid.setSiteID(pdu.getHeader().getRadioReferenceID().getSiteID());
    eid.setEntityID(pdu.getHeader().getRadioReferenceID().getEntityID());
    entityIDDisplayPanel.setData(eid);

    radioIDTF.setText("" + pdu.getHeader().getRadioReferenceID());
    encodingTF.setText("" + pdu.getEncodingScheme());
    tdlTF.setText(pdu.getTdlType().getDescription());
    sampleRateTF.setText("" + pdu.getSampleRate());
    dataLenTF.setText("" + pdu.getDataLength());
    samplesTF.setText("" + pdu.getSamples());
  }

